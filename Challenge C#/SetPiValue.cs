namespace CSharpDiscovery.Quest01
{
    public class SetPiValue_Exercice
    {
        public static float SetPiValue(float pi)
        {

            // float equal 7 digits
            // double equal 15-16 digits
            // decimal equal 27-28 digits
            float x = 3.14159f;
            float result = x + pi ;

            return result;
        }
    }
}